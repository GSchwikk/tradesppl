=begin
    def job_title_icon
        if @user.profile.job_title == "Plumber"
            "<i class='fa fa-wrench'></i>".html_safe
        elsif @user.profile.job_title == "Electrician"
            "<i class='fa fa-bolt'></i>".html_safe
        elsif @user.profile.job_title == "Painter"
            "<i class='fa fa-paint-brush'></i>".html_safe
       end
    end=end


